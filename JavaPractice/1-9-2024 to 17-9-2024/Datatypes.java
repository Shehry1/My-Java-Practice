public class Datatypes{
    public static void main(String[] args)
    {
        int myNumber = 12;
        
        int yourNumber;
        
        yourNumber = 123;
        System.out.println(myNumber);
        System.out.println(yourNumber);
        yourNumber = 34; //It will take the value of yourNumber of this line because it's update
        System.out.println(yourNumber);
        
        //Bolean Datatype
        boolean isVegeterian = false;
        System.out.println(isVegeterian);
        
        isVegeterian = true;
        System.out.println(isVegeterian);
        
        //Float datatype
        float myFloat = 5.0f;
        System.out.println(myFloat);
        
        //char datatypes
        char myChar = 'A';
        System.out.println(myChar);
        
        //string datatypes
        //We will write The s of string in capital latter lake this String
        String myString = "Shehriyar Ahmad";
        System.out.println(myString);
        
    }
}